void main(){
  int num=20;
  if(num>50){
    if(num%10==0){
      print('num is greater than 50 and is divisible by 10');
    }else{
      print('num is greater than 50 and is not divisible by 10');
    }
  }else{
    if(num%10==0){
      print('num is less than 50 and is divisible by 10');
    }else{
      print('num is less than 50 and is not divisible by 10');
    }
  }
}